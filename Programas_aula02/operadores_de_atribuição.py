# atribuição simples, vamos dizer que temos uma variavel chamada dinheiro 
# e nos vamos atribuir o valor de 900 reais a ela fazermos isso usando o sinal de igual

Dinheiro = 900
print(Dinheiro)

# atribuição com adição, vamos dizer que nosso dinheiro é 500 e que a gente quer atribuit 
# a ele mais 450, sera do jeito que esta abaixo que sera feito, sendo necessario somente 
# a troca do operador aritimetico para subitrair, multiplicar, divisão, divisão inteira,
# exponenciação e modulo(que seria o resto da divisão)

#soma
Dinheiro = 900
Dinheiro +=450
print(Dinheiro)

#subtração
Dinheiro = 900
Dinheiro -=450
print(Dinheiro)

#multiplicação
Dinheiro = 900
Dinheiro *=450
print(Dinheiro)

#divisão
Dinheiro = 900
Dinheiro /=450
print(Dinheiro)

#divisão inteira
Dinheiro = 900
Dinheiro //=450
print(Dinheiro)

#exponenciação
Dinheiro = 900
Dinheiro **= 45
print(Dinheiro)

#modulo
Dinheiro = 900
Dinheiro %=450
print(Dinheiro)

