# igualdade sempre retornara um valor booleano, a gentre vai comparar se o valor
# do saldo é igual ao valor do saque
saldo = 290
saque = 280

print(saldo == saque)

# Já a diferença a gente vai comparar para ver e é diferente e se for diferente 
# retornara true caso nao for retornara falso 

saldo = 290
saque = 280

print(saldo != saque)

# ja no caso do > ou >= ele vai comparar se o saldo é maior que o saque ou se o saldo 
# é maior ou igaul ao valor do saque retornando um valor true ou false 
# da mesma maneira temos o < ou <= que usa a mesma logica 

saldo = 290
saque = 2990

print(saldo > saque)
print(saldo >= saque)
print(saldo < saque)
print(saldo <= saque)

